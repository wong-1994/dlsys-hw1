class ReLU(TensorOp):
    def compute(self, a):
        ### BEGIN YOUR SOLUTION
        return array_api.maximum(a, 0)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        gate = array_api.where(node.inputs[0].cached_data > 0, 1, 0)
        out_grad.cached_data *= gate
        return out_grad
        ### END YOUR SOLUTION


def relu(a):
    return ReLU()(a)

